#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*last;

	if (!lst)
		return (NULL);
	last = lst;
	while (last)
	{
		if (!last->next)
			return (last);
		last = last->next;
	}
	return (last);
}
